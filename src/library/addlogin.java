    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean isNotEmpty = !jTextField1.getText().isEmpty()
                && !jTextField2.getText().isEmpty()
                && !jTextField4.getText().isEmpty()
                && !jTextField5.getText().isEmpty();
        if(isNotEmpty)
        {
        try {
            PreparedStatement pstr;
            String sql = "insert into login(username, password, role, phoneno, NIC) values(?,?,?,?,?)";
            pstr = conn.prepareStatement(sql);
            pstr.setString(1, jTextField1.getText());
            pstr.setString(2, jTextField2.getText());
            if (jComboBox1.getSelectedIndex() == 0) {
                pstr.setString(3, "librarian");
            }
            else
            {
                pstr.setString(3, "admin");
            }
            pstr.setInt(4, Integer.parseInt(jTextField4.getText()));
            pstr.setInt(5, Integer.parseInt(jTextField5.getText()));
            int row = pstr.executeUpdate();
            JOptionPane.showMessageDialog(this, "Successfully added!");
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

