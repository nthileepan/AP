    public void login(){
        String username = user.getText();
        String password = new String(pass.getPassword());
        try {
            str = conn.createStatement();
            String sqlQuery = "Select * from login where username = '" + username + "' and password = '" + password + "'";
            re = str.executeQuery(sqlQuery);
            if (re.next() == true) {
                String role = re.getString("role");
                loger(username, "login", new Timestamp(System.currentTimeMillis()));
                dashboard d = new dashboard(username,role);
                d.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Wrong password!");
            }
            re.close();
            str.close();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }

